// Передчуття, ким стане (стале для одного яйця — на основі імені).
const MYSTERY = [
  "галасливе", "хитре", "надзвичайно ласкаве", "хаотичне",
  "загадкове", "надміру драматичне", "підозріло розумне",
];

export function eggMystery(name = "") {
  let h = 0;
  for (const ch of name) h = (h + ch.codePointAt(0)) % 9973;
  return MYSTERY[h % MYSTERY.length];
}

export function randomLine(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}
