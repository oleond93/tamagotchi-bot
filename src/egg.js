function hashName(name = "") {
  let h = 0;
  for (const ch of name) h = (h + ch.codePointAt(0)) % 9973;
  return h;
}

export function eggMystery(temperament = "balanced", name = "") {
  const list = MYSTERY[temperament] || MYSTERY.balanced;
  return list[hashName(name) % list.length];
}

export function randomLine(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}
